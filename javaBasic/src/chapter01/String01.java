package chapter01;

public class String01 {
	public static void main(String[] args) {
		// 문자열
		// 문장 혹은 단어를 쉽게 표기하기 위한 Java Object Class
		// 기본 데이터 타입이 아님!!
		
		// 생성
		String str1;
		// 초기화
		str1 = new String();
		str1 = "apple";
		System.out.println(str1); // apple
		
		// 생성 및 초기화
		String str2 = "banana";
		System.out.println(str2); // banana
		
	} // end of main
} // end of class
